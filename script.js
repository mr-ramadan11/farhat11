function stopQuizTimer({ reset = false } = {}) {
  if (quizTimerStartedAt !== null) {
    quizElapsedBeforeCurrentRun += Date.now() - quizTimerStartedAt;
    quizTimerStartedAt = null;
  }

  clearQuizTimerInterval();

  if (reset) {
    quizElapsedBeforeCurrentRun = 0;
  }
}

